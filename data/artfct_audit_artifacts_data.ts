const generateTimestamp = (daysAgo: number): string => {
    const date = new Date();
    date.setDate(date.getDate() - daysAgo);
    date.setHours(date.getHours() - Math.random() * 24);
    date.setMinutes(date.getMinutes() - Math.random() * 60);
    return date.toISOString();
};

